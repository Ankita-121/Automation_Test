package Hometask1;

public class UnevenArray {

	public static void main(String[] args) {
		int a[][] = {
				{ 1, 9, 4 },
			          { 0, 2},
			          { 0, 1, 2, 3, 4 }};
			          for (int row[]:a) {
			        	  for(int e:row) {
			        		  System.out.print(e);
			        		  }
			        	  System.out.println();
			          }
			   

	}
}



